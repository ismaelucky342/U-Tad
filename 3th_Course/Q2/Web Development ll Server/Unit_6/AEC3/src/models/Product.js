const mongoose = require('mongoose');

const productSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: [true, 'El nombre del producto es requerido'],
      trim: true,
      minlength: [3, 'El nombre debe tener al menos 3 caracteres'],
      maxlength: [100, 'El nombre no puede exceder 100 caracteres']
    },
    description: {
      type: String,
      required: [true, 'La descripci√≥n es requerida'],
      minlength: [10, 'La descripci√≥n debe tener al menos 10 caracteres'],
      maxlength: [500, 'La descripci√≥n no puede exceder 500 caracteres']
    },
    price: {
      type: Number,
      required: [true, 'El precio es requerido'],
      min: [0, 'El precio no puede ser negativo'],
      max: [999999, 'El precio es demasiado alto']
    },
    ingredients: {
      type: String,
      required: [true, 'Los ingredientes son requeridos'],
      minlength: [3, 'Los ingredientes deben tener al menos 3 caracteres']
    },
    available: {
      type: Boolean,
      default: true
    },
    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true
    }
  },
  { timestamps: true }
);

module.exports = mongoose.model('Product', productSchema);
