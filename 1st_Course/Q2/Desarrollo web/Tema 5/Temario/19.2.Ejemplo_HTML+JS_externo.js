// Definición de la función sumar
function sumar(a, b) {
    var suma = a + b;
    return suma;
}

// Llamada a la función sumar con los argumentos 2 y 3
var resultado1 = sumar(2, 3);
console.log("Resultado 1: " + resultado1);

// Llamada a la función sumar con los argumentos 5 y 7
var resultado2 = sumar(5, 7);
console.log("Resultado 2: " + resultado2);

// Llamada a la función sumar con los argumentos 1 y 2
var resultado3 = sumar(1, 2);
console.log("Resultado 3: " + resultado3);
