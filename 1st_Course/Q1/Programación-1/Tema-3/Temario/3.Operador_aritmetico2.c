#include <stdio.h>

int main() {
    // Definición de variables
    int a = 10;
    int b = 3;

    // Modulo
    int modulo = a % b;
    printf("El modulo es: %d\n", modulo);

    // Incremento
    int incremento = a++;
    printf("El valor después del incremento es: %d\n", incremento);

    // Decremento
    int decremento = a--;
    printf("El valor después del decremento es: %d\n", decremento);

    return 0;
}
/*LOS TEMAS DE DECREMENTO/INCREMENTO FUERON USADOS EN 42*/
