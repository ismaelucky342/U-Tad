/*Calculo area rectangulo */

#include<stdio.h>

int     main()
{
    int altura, base; 
    printf("introduce  la base del rectangulo: \n");
    scanf("%d", &base);
    printf("introduce altura del rectangulo: \n");
    scanf("%d", &altura);
    printf("el resultado es: %d\n",base*altura ); 
    return 0; 
}