# Operaciones aritméticas básicas en R

# Suma
suma <- 5 + 3
print(paste("Suma: 5 + 3 =", suma))

# Resta
resta <- 10 - 4
print(paste("Resta: 10 - 4 =", resta))

# Multiplicación
multiplicacion <- 7 * 6
print(paste("Multiplicación: 7 * 6 =", multiplicacion))

# División
division <- 20 / 4
print(paste("División: 20 / 4 =", division))

# Potenciación
potenciacion <- 2 ^ 3
print(paste("Potenciación: 2 ^ 3 =", potenciacion))

# Módulo
modulo <- 10 %% 3
print(paste("Módulo: 10 %% 3 =", modulo))

# División entera
division_entera <- 10 %/% 3
print(paste("División entera: 10 %/% 3 =", division_entera))