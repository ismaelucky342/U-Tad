public class OperatorsID {
    public static void main(String args []) {
        int c = 5; 
        //postincremento
        System.out.println(c); // imprime 5 
        System.out.println(c++); //imprime 5
        System.out.println(c); //imprime 6

        //preincremento
        System.out.println(c); //imprime 6 
        System.out.println(++c); //imprime 7
        System.out.println(c);  //imprime 7

    }
}