# Operadores comparativos en R

# Igual que (==)
resultado_igual <- 5 == 5
print(resultado_igual)  # TRUE

# Distinto de (!=)
resultado_distinto <- 5 != 3
print(resultado_distinto)  # TRUE

# Menor que (<)
resultado_menor <- 3 < 5
print(resultado_menor)  # TRUE

# Menor o igual que (<=)
resultado_menor_igual <- 5 <= 5
print(resultado_menor_igual)  # TRUE

# Mayor que (>)
resultado_mayor <- 5 > 3
print(resultado_mayor)  # TRUE

# Mayor o igual que (>=)
resultado_mayor_igual <- 5 >= 3
print(resultado_mayor_igual)  # TRUE